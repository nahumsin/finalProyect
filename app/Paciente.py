class Paciente:
    folio = 0
    nombre = ""
    apellido = ""
    edad = ""
    email = ""
    direccion = ""
    
    def setFolio(self,folio):
        self.folio = folio
    
    def setNombre(self,nombre):
        self.nombre = nombre
    
    def setApellido(self,apellido):
        self.apellido = apellido
    
    def setEdad(self,edad):
        self.edad = edad
    
    def setEmail(self,email):
        self.email = email
        
    def setDireccion(self,direccion):
        self.direccion = direccion
        
    def getFolio():
        return folio
        
    def getNombre():
        return nombre
    
    def getApellido():
        return apellido
    
    def getEdad():
        return edad
       
    def getEmail():
        return email
        
    def getDireccion():
        return direccion
